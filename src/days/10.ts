import { Test } from '.';

const regExp = /(\(\))|(\[\])|<>|(\{\})/

const corruptedPoints = {
  ")": 3,
  "]": 57,
  "}": 1197,
  ">": 25137,
}

const incompletePoints = {
  "(": 1,
  "[": 2,
  "{": 3,
  "<": 4,
}

const removeComplete = (line: string) => {
  let rest = line
  let found = rest.match(regExp)
  while (found) {
    rest = rest.replace(regExp, '')
    found = rest.match(regExp)
  }
  const [illegal] = rest.match(/\)|\}|\]|>/) || []
  return { illegal, rest }
}

export const first = (inputString: string) => {
  const lines = inputString.split('\n')
  const sum = lines
    .map(removeComplete)
    .filter(({ illegal }) => !!illegal)
    .reduce((acc, { illegal }) => acc + corruptedPoints[illegal], 0)

  return sum
}

export const second = (inputString: string) => {
  const lines = inputString.split('\n')
  const sum = lines
    .map(removeComplete)
    .filter(({ illegal }) => !illegal)
    .map(({ rest }) =>
      rest
        .split('')
        .reduceRight((acc, curr) => acc * 5 + incompletePoints[curr], 0)
    )
  sum.sort((a, b) => b - a)
  const middle = Math.floor(sum.length / 2)

  return sum[middle]
}

